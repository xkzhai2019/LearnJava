package com.xkzhai.string;

public class Person {
	private String name;
	private int age;
	private boolean sex;
	public String getName() {
		return name;
	}
	public Person setName(String name) {
		this.name = name;
		return this;
	}
	public int getAge() {
		return age;
	}
	public Person setAge(int age) {
		this.age = age;
		return this;
	}
	public boolean isSex() {
		return sex;
	}
	public Person setSex(boolean sex) {
		this.sex = sex;
		return this;
	}
	
	
}
